print(1234) #por ser dinâmico ele já sabe o tipo

# Aspas simples
print('Luiz Otávio')
print(1, 'Luiz "Otávio"')

# Aspas duplas
print("Luiz Otávio")
print(2, "Luiz 'Otávio'")

# Escape
print("Luiz \"Otávio\"") # falo pra ele pular o caracter depois da barra

# r
print(r"Luiz \"Otávio\"") # quer que o caracter de escape apareça